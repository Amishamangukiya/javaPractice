package week7homework;

public class ComparisonOperators {
    public static void main(String[] args) {
        int x=5;
        int y=7;
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x>=y);
        System.out.println(x<=y);
    }
}
